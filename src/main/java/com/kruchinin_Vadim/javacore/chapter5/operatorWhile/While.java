package com.kruchinin_Vadim.javacore.chapter5.operatorWhile;

// Продемострировать применение оператора while
public class While {
    public static void main(String[] args) {
        int n = 10;

        while (n > 0) {
            System.out.println("такт " + n);
            n--;
        }
    }
}
