package com.kruchinin_Vadim.javacore.chapter7.classString;

// Продемонстрировать применение символьных строк
class StringDemo {
    public static void main(String[] args) {
        String strOb1 = "Первая строка";
        String strOb2 = "Вторая строка";
        String strOb3 = strOb1 + " и " + strOb2;

        System.out.println(strOb1);
        System.out.println(strOb2);
        System.out.println(strOb3);
    }
}
/*
    В данном классе создается объект класса String
    А так же применяется конкатенация (+) строк
 */
